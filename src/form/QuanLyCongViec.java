    private void btnXoaCongViecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaCongViecActionPerformed
        // TODO add your handling code here:
        index = listCongViec.getSelectedRow();
        int check = list.size();
        if (check == 0) {
            JOptionPane.showMessageDialog(rootPane, "Danh sách công việc không còn để xóa");
        } else if (index < 0) {
            JOptionPane.showMessageDialog(rootPane, "Hãy chọn công việc để xóa");
        } else {
            if (new DAO().XoaCongViec(list.get(index))) {
                list.remove(index);
                JOptionPane.showMessageDialog(rootPane, "Xóa thành công");
                showTable();
            }
        }
    }//GEN-LAST:event_btnXoaCongViecActionPerformed

    private void btnSuaCongViecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaCongViecActionPerformed
        // TODO add your handling code here:
        index = listCongViec.getSelectedRow();
        int check = list.size();
        if (check == 0) {
            JOptionPane.showMessageDialog(rootPane, "Không có công việc để sửa!");
        } else if (index < 0) {
            JOptionPane.showMessageDialog(rootPane, "Hãy chọn công việc để sửa!");
        } else {
            SuaCongViec suacongviec = new SuaCongViec(this, rootPaneCheckingEnabled);
            suacongviec.setEditData(list.get(index));
            suacongviec.setVisible(true);
        }
    }//GEN-LAST:event_btnSuaCongViecActionPerformed

