    public void showTable() {
        model.setRowCount(0);
        for (CongViec congviec : list) {
            model.addRow(new Object[]{
                congviec.getID(), congviec.getTenCongViec(), formatDate.format(congviec.getNgayKhoiTao()), formatDate.format(congviec.getNgayHetHan()), congviec.getNgayHoanThanh(), congviec.getMoTaCongViec(), congviec.getTrangThai()
            });
        }
    }

    public void ThemCongViec(CongViec congviec) {
        list.add(congviec);
        showTable();
    }

